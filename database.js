function getPrice(price) {

    return Number(price.replace("$", ""))
    
}

function displayProduct(clothes) {

    clothesContainer.innerHTML = ""

    clothes.forEach((label) => {

        const labelElement = document.createElement("div")

        labelElement.classList.add("card")

        labelElement.innerHTML = `<img src=${label.pic} />
        <div class="info">
          <div class="title">${label.title}</div>
          <div class="price">${label.price}</div>
          <button class="masukin">Masukin Keranjang</button>
        </div>`

        const tombol = labelElement.querySelector(".masukin")

        tombol.addEventListener("click", () => {

        const found = keranjang.find(item => item.title === label.title)

        if (found) {

            found.qty += 1

        } else {

            keranjang.push({
                ...label,
                qty: 1
            })
        }

        updateCart()
        })


        clothesContainer.appendChild(labelElement)

    })

}

function updateCart() {

    listKeranjang.innerHTML = ""
    let total = 0

    keranjang.forEach((item, index) => {

        const li = document.createElement("li")

        li.innerHTML = `
            ${item.title} x ${item.qty} - $${getPrice(item.price) * item.qty}
            <button class="hapus">‚ùå</button>`

        li.querySelector(".hapus").addEventListener("click", () => {

            if (item.qty > 1) {
                item.qty -= 1
            } else {
                keranjang.splice(index, 1)
            }

            updateCart()
        })

        listKeranjang.appendChild(li)

        total += getPrice(item.price) * item.qty
    })

    totalPrice.textContent = `Total: $${total}`
}

